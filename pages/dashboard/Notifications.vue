  <v-container fluid fill-height>
    <v-row align="center" justify="center">
      <v-col cols="12">
        <material-card color="primary">
          <template v-slot:header>
            <div class="px-3">
              <div class="title font-weight-light mb-2">
                Notifications
              </div>

              <div class="category">
